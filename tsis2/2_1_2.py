list1 = ["apple", "banana", "cherry"]
print(list1)

#lists allow us to use duplicate values
list2 = ["apple", "banana", "cherry", "apple"]
print(list2)

#list's length
print(len(list1))

#list values can be any data type
list3 = [1, 2, 3, 4]
list4 = [2j, 3j, 5j]
list5 = [True, False, True]

#also in the list there are can be values with different data types
list6 = ["Hello", 5j, 3, True]
print(list6)

#list - type
list1 = ["apple", "banana", "cherry"]
print(type(list1))

#access items
print(list1[2])

#negative indexes
print(list1[-2])

#range of indexes
list7 = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(list7[1:4])

print(list7[:4])

print(list7[2:])